#ifndef CPP_SKELETON_UTILS_H
#define CPP_SKELETON_UTILS_H


class Utils {
public:

    static int int2string(){

    }

    static int string2int(const std::string stringToTranslate){

        return stoi(stringToTranslate);
    }

    static float string2float(const std::string stringToTranslate){
        return stof(stringToTranslate);
    }

private:

};

#endif //CPP_SKELETON_UTILS_H
